    private void listDisplayAllEmployeesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listDisplayAllEmployeesMouseClicked
        //When user clicks an item on the list, the corresponding information is displayed
        selectedItem = (String)listDisplayAllEmployees.getSelectedValue();
        //finds item clicked by user in hash tabel and displays it's contents
        selectedItem = FrameMain.getFirstWord(selectedItem); //updates selected item to employee number only
        
        //searches for employee in hash table and then displays employee info
        //stores bucket and position in list of employee in variables "currentBucket" and "position"
        int currentBucket = FrameMain.getEmployeeHashTable().calcBucket(Integer.parseInt(FrameMain.getFirstWord(selectedItem)));
        int position = FrameMain.getEmployeeHashTable().searchEmployee(Integer.parseInt(FrameMain.getFirstWord(selectedItem)));
        EmployeeInfo currentEmployee = FrameMain.getEmployeeHashTable().getBuckets()[currentBucket].get(position);//stores the employee of interest
        
        //displays information of employee
        labelDisplayEmployeeNumberAnswer.setText(String.valueOf(currentEmployee.getEmployeeNumber()));
        labelDisplayFirstNameAnswer.setText(currentEmployee.getFirstName());
        labelDisplayLastNameAnswer.setText(currentEmployee.getLastName());
        labelDisplaySexAnswer.setText(getSexString(currentEmployee.getSex()));
        labelDisplayWorkLocationAnswer.setText(getWorkLocationString(currentEmployee.getWorkLocation()));
        labelDisplayDeductionsRateAnswer.setText(String.format("%.2f",currentEmployee.getDeductionsRate()));
        
        //if the employee is part time
        if (currentEmployee instanceof PartTimeEmployee) {
            labelDisplayEmployeeTypeAnswer.setText("Part Time");
            labelDisplayHourlyWageAnswer.setText(String.format("%.2f",((PartTimeEmployee)currentEmployee).getHourlyWage()));
            labelDisplayHoursPerWeekAnswer.setText(String.format("%.2f",((PartTimeEmployee)currentEmployee).getHoursPerWeek()));
            labelDisplayWeeksPerYearAnswer.setText(String.format("%.2f",((PartTimeEmployee)currentEmployee).getWeeksPerYear()));
            labelDisplayAnnualGrossIncomeAnswer.setText(String.format("%.2f",((PartTimeEmployee)currentEmployee).calcAnnualGrossIncome()));
            panelDisplayFullTime.setVisible(false);
            panelDisplayPartTime.setVisible(true);
            labelDisplayError.setVisible(false);
        }
        //if the employee is full time
        else if (currentEmployee instanceof FullTimeEmployee) {
            labelDisplayEmployeeTypeAnswer.setText("Full Time");
            labelDisplayAnnualSalaryAnswer.setText(String.format("%.2f",((FullTimeEmployee)currentEmployee).getAnnualSalary()));
            labelDisplayAnnualGrossIncomeAnswer.setText(String.format("%.2f",((FullTimeEmployee)currentEmployee).calcAnnualGrossIncome()));
            panelDisplayFullTime.setVisible(true);
            panelDisplayPartTime.setVisible(false);
            labelDisplayError.setVisible(false);
        }
        //there is an error; employee is neither part time or fulltime
        else {
            labelDisplayError.setText("ERROR: Employee does not exist.");
            labelDisplayError.setVisible(true);
        }
        //sets visibility of panels
        panelDisplayEmployee.setVisible(true);//employee information is visible
        labelDisplayErrorNoEmployee.setVisible(false);//there is an employee selected
    }//GEN-LAST:event_listDisplayAllEmployeesMouseClicked
    
