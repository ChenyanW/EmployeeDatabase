    //method which returns the string value of sex
    public String getSexString (int sex){
        if (sex == 0){
            return ("Male");
        }
        else if (sex == 1){
            return ("Female");
        }
        else{
            return ("Other");
        }
    }
    
    //method which returns the string value of work location
    public String getWorkLocationString (int workLocation){
        if (workLocation == 0){
            return ("Missisauga");
        }
        else if (workLocation == 1){
            return ("Ottawa");
        }
        else{
            return ("Chicago");
        }
    }
    
